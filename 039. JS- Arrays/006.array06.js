//Array.from() – Cria um array a partir de algo semelhante a um array (como uma string ou um objeto iterável).
let texto = "Hello";
let arrayLetras = Array.from(texto);

console.log(arrayLetras); // ['H', 'e', 'l', 'l', 'o']

//Array.of() – Cria um array a partir dos argumentos passados.
let numeros = Array.of(1, 2, 3, 4, 5);
console.log(numeros); // [1, 2, 3, 4, 5]



