// Usando Math.min() para encontrar o menor número em uma lista
let menor = Math.min(10, 5, 8, 3, 20);
console.log("Math.min():", menor);

// Usando Math.max() para encontrar o maior número em uma lista
let maior = Math.max(10, 5, 8, 3, 20);
console.log("Math.max():", maior);

// Usando Math.pow() para calcular potência (base ^ expoente)
let potencia = Math.pow(2, 3); // 2³ = 8
console.log("Math.pow():", potencia);

// Usando Math.round() para arredondar um número para o inteiro mais próximo
let arredondado = Math.round(4.7);
console.log("Math.round():", arredondado);

// Usando Math.sqrt() para calcular a raiz quadrada
let raizQuadrada = Math.sqrt(25); // √25 = 5
console.log("Math.sqrt():", raizQuadrada);

// Usando Math.cbrt() para calcular a raiz cúbica
let raizCubica = Math.cbrt(27); // ∛27 = 3
console.log("Math.cbrt():", raizCubica);

// Usando Math.random() para gerar um número aleatório entre 0 e 1
let aleatorio = Math.random();
console.log("Math.random():", aleatorio);
